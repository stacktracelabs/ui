function componentSlug(name) {
  return name
    .replace(/([A-Z]+)([A-Z][a-z])/g, '$1-$2')
    .replace(/([a-z0-9])([A-Z])/g, '$1-$2')
    .toLowerCase()
}

function markdownAnchor(title) {
  return title
    .toLowerCase()
    .replace(/[`*_]/g, '')
    .replace(/[^a-z0-9\s-]/g, '')
    .trim()
    .replace(/\s+/g, '-')
}

function normalizeInline(value) {
  return value.replace(/\s+/g, ' ').trim()
}

function normalizeExample(value) {
  return value
    .replaceAll('@/Components/Base/', '@/Components/')
    .replaceAll('@/Composables/Base/', '@/Composables/')
    .trim()
}

function absoluteDocumentationLink(href) {
  return href.startsWith('/docs') ? `${hostedDocsBase}${href}` : href
}

function sourceFilePath(importPath, pagePath) {
  const cleanPath = importPath.replace(/\?raw$/, '')

  if (cleanPath.startsWith('@/')) {
    return join(docsDirectory, 'resources/js', cleanPath.slice(2))
  }

  return resolve(dirname(pagePath), cleanPath)
}

function createScriptContext(script, pagePath) {
  const sourceFile = ts.createSourceFile(
    pagePath,
    script,
    ts.ScriptTarget.Latest,
    true,
    ts.ScriptKind.TS,
  )
  const declarations = new Map()
  const values = new Map()

  for (const statement of sourceFile.statements) {
    if (ts.isImportDeclaration(statement)) {
      const moduleName = statement.moduleSpecifier.text
      const identifier = statement.importClause?.name?.text

      if (identifier && moduleName.endsWith('?raw')) {
        values.set(identifier, readFileSync(sourceFilePath(moduleName, pagePath), 'utf8'))
      }

      continue
    }

    if (!ts.isVariableStatement(statement)) {
      continue
    }

    for (const declaration of statement.declarationList.declarations) {
      if (ts.isIdentifier(declaration.name) && declaration.initializer) {
        declarations.set(declaration.name.text, declaration.initializer)
      }
    }
  }

  function evaluate(node, resolving = new Set()) {
    if (!node) {
      return undefined
    }

    if (ts.isStringLiteral(node) || ts.isNoSubstitutionTemplateLiteral(node)) {
      return node.text
    }

    if (ts.isNumericLiteral(node)) {
      return Number(node.text)
    }

    if (node.kind === ts.SyntaxKind.TrueKeyword) {
      return true
    }

    if (node.kind === ts.SyntaxKind.FalseKeyword) {
      return false
    }

    if (ts.isParenthesizedExpression(node) || ts.isAsExpression(node) || ts.isSatisfiesExpression(node)) {
      return evaluate(node.expression, resolving)
    }

    if (ts.isIdentifier(node)) {
      if (values.has(node.text)) {
        return values.get(node.text)
      }

      if (!declarations.has(node.text) || resolving.has(node.text)) {
        return undefined
      }

      const nextResolving = new Set(resolving).add(node.text)
      const value = evaluate(declarations.get(node.text), nextResolving)

      if (value !== undefined) {
        values.set(node.text, value)
      }

      return value
    }

    if (node.kind === ts.SyntaxKind.NullKeyword) {
      return null
    }

    if (ts.isArrayLiteralExpression(node)) {
      const values = []

      for (const element of node.elements) {
        if (ts.isSpreadElement(element)) {
          const spread = evaluate(element.expression, resolving)

          if (!Array.isArray(spread)) {
            return undefined
          }

          values.push(...spread)
          continue
        }

        const value = evaluate(element, resolving)

        if (value === undefined) {
          return undefined
        }

        values.push(value)
      }

      return values
    }

    if (ts.isObjectLiteralExpression(node)) {
      const value = {}

      for (const property of node.properties) {
        if (ts.isSpreadAssignment(property)) {
          const spread = evaluate(property.expression, resolving)

          if (!spread || typeof spread !== 'object' || Array.isArray(spread)) {
            return undefined
          }

          Object.assign(value, spread)
          continue
        }

        if (ts.isShorthandPropertyAssignment(property)) {
          const propertyValue = evaluate(property.name, resolving)

          if (propertyValue === undefined) {
            return undefined
          }

          value[property.name.text] = propertyValue
          continue
        }

        if (!ts.isPropertyAssignment(property)) {
          return undefined
        }

        const propertyName = property.name.text
        const propertyValue = evaluate(property.initializer, resolving)

        if (propertyName === undefined || propertyValue === undefined) {
          return undefined
        }

        value[propertyName] = propertyValue
      }

      return value
    }

    if (ts.isPropertyAccessExpression(node)) {
      const receiver = evaluate(node.expression, resolving)

      return receiver && typeof receiver === 'object'
        ? receiver[node.name.text]
        : undefined
    }

    if (ts.isBinaryExpression(node) && node.operatorToken.kind === ts.SyntaxKind.PlusToken) {
      const left = evaluate(node.left, resolving)
      const right = evaluate(node.right, resolving)
      return left === undefined || right === undefined ? undefined : `${left}${right}`
    }

    if (ts.isTemplateExpression(node)) {
      let value = node.head.text

      for (const span of node.templateSpans) {
        const expression = evaluate(span.expression, resolving)

        if (expression === undefined) {
          return undefined
        }

        value += `${expression}${span.literal.text}`
      }

      return value
    }

    if (ts.isCallExpression(node) && ts.isPropertyAccessExpression(node.expression)) {
      const receiver = evaluate(node.expression.expression, resolving)
      const method = node.expression.name.text
      const args = node.arguments.map(argument => evaluate(argument, resolving))

      if (receiver === undefined || args.some(argument => argument === undefined)) {
        return undefined
      }

      if (method === 'join' && Array.isArray(receiver)) {
        return receiver.join(args[0] ?? ',')
      }

      if (method === 'replaceAll' && typeof receiver === 'string') {
        return receiver.replaceAll(args[0], args[1])
      }

      if (method === 'replace' && typeof receiver === 'string') {
        return receiver.replace(args[0], args[1])
      }

      if (method === 'trim' && typeof receiver === 'string') {
        return receiver.trim()
      }
    }

    return undefined
  }

  function evaluateExpression(expression) {
    const expressionSource = ts.createSourceFile(
      'expression.ts',
      `const value = (${expression})`,
      ts.ScriptTarget.Latest,
      true,
      ts.ScriptKind.TS,
    )
    const statement = expressionSource.statements[0]
    const initializer = statement?.declarationList?.declarations?.[0]?.initializer

    return evaluate(initializer)
  }

  return { evaluateExpression }
}

function staticProp(node, name) {
  const prop = node.props?.find(candidate => candidate.type === NODE.ATTRIBUTE && candidate.name === name)
  return prop?.value?.content
}

function boundProp(node, name, context) {
  const prop = node.props?.find(candidate => (
    candidate.type === NODE.DIRECTIVE
      && candidate.name === 'bind'
      && candidate.arg?.type === NODE.SIMPLE_EXPRESSION
      && candidate.arg.content === name
  ))

  return prop?.exp?.content ? context.evaluateExpression(prop.exp.content) : undefined
}

function propValue(node, name, context) {
  return staticProp(node, name) ?? boundProp(node, name, context)
}

function findElement(node, tag) {
  if (node.type === NODE.ELEMENT && node.tag === tag) {
    return node
  }

  for (const child of node.children ?? []) {
    if (typeof child === 'object') {
      const match = findElement(child, tag)

      if (match) {
        return match
      }
    }
  }

  return undefined
}

function inlineContent(node) {
  if (typeof node === 'string') {
    return node
  }

  if (node.type === NODE.TEXT) {
    return node.content
  }

  if (node.type === NODE.INTERPOLATION) {
    return `{{ ${node.content.content} }}`
  }

  if (node.type === NODE.COMPOUND_EXPRESSION) {
    return node.children.map(inlineContent).join('')
  }

  if (node.type !== NODE.ELEMENT) {
    return ''
  }

  const content = normalizeInline((node.children ?? []).map(inlineContent).join(''))

  if (node.tag === 'code') {
    return `\`${content.replaceAll('`', '\\`')}\``
  }

  if (node.tag === 'em' || node.tag === 'i') {
    return `*${content}*`
  }

  if (node.tag === 'strong' || node.tag === 'b') {
    return `**${content}**`
  }

  if (node.tag === 'Link' || node.tag === 'a') {
    const href = staticProp(node, 'href')
    return href ? `[${content}](${absoluteDocumentationLink(href)})` : content
  }

  if (node.tag === 'br') {
    return '\n'
  }

  return content
}

function fencedCode(code, language = 'text', filename) {
  const label = filename ? `*${filename}*\n\n` : ''
  return `${label}\`\`\`${language}\n${normalizeExample(code)}\n\`\`\``
}

function escapeTableCell(value) {
  return String(value)
    .replaceAll('|', '\\|')
    .replaceAll('\n', '<br>')
}

function renderApiTable(entries, nameLabel = 'Property', showDefault = false) {
  if (!Array.isArray(entries) || !entries.length) {
    return ''
  }

  const includeDefault = showDefault || entries.some(entry => entry.default !== undefined)
  const headings = includeDefault
    ? `| ${nameLabel} | Type | Default | Description |`
    : `| ${nameLabel} | Type | Description |`
  const separators = includeDefault
    ? '| --- | --- | --- | --- |'
    : '| --- | --- | --- |'
  const rows = entries.map(entry => {
    const name = `\`${escapeTableCell(entry.name)}\`${entry.required ? ' (required)' : ''}`
    const type = `\`${escapeTableCell(entry.type)}\``
    const description = escapeTableCell(entry.description)

    if (!includeDefault) {
      return `| ${name} | ${type} | ${description} |`
    }

    const defaultValue = entry.default === undefined
      ? '—'
      : `\`${escapeTableCell(entry.default)}\``

    return `| ${name} | ${type} | ${defaultValue} | ${description} |`
  })

  return [headings, separators, ...rows].join('\n')
}

function referenceHeading(node, context) {
  const level = Number(propValue(node, 'heading-level', context) ?? 2)
  const name = propValue(node, 'name', context)

  return `${'#'.repeat(level)} \`${name}\``
}

function renderNode(node, context) {
  if (node.type !== NODE.ELEMENT) {
    return ''
  }

  if (/^h[2-4]$/.test(node.tag)) {
    const level = Number(node.tag.slice(1))
    return `${'#'.repeat(level)} ${normalizeInline((node.children ?? []).map(inlineContent).join(''))}`
  }

  if (node.tag === 'p') {
    return normalizeInline((node.children ?? []).map(inlineContent).join(''))
  }

  if (node.tag === 'ul' || node.tag === 'ol') {
    let index = 0
    return (node.children ?? [])
      .filter(child => child.type === NODE.ELEMENT && child.tag === 'li')
      .map(child => {
        index += 1
        const marker = node.tag === 'ol' ? `${index}.` : '-'
        return `${marker} ${normalizeInline((child.children ?? []).map(inlineContent).join(''))}`
      })
      .join('\n')
  }

  if (node.tag === 'CodeBlock') {
    const code = propValue(node, 'code', context)

    if (typeof code !== 'string') {
      throw new Error(`Unable to resolve CodeBlock source in ${context.pagePath}`)
    }

    return fencedCode(
      code,
      propValue(node, 'language', context) ?? 'text',
      propValue(node, 'filename', context),
    )
  }

  if (node.tag === 'ComponentPreview') {
    const source = propValue(node, 'source', context)

    if (typeof source !== 'string') {
      throw new Error(`Unable to resolve ComponentPreview source in ${context.pagePath}`)
    }

    const title = propValue(node, 'title', context)
    const label = title ? `**${title} example**\n\n` : ''
    return `${label}${fencedCode(source, 'vue')}`
  }

  if (node.tag === 'ApiReferenceTable') {
    return renderApiTable(
      propValue(node, 'entries', context),
      propValue(node, 'name-label', context) ?? 'Property',
      propValue(node, 'show-default', context) ?? false,
    )
  }

  if (node.tag === 'FunctionApiReference') {
    const description = propValue(node, 'description', context)
    const signature = propValue(node, 'signature', context)
    const parameters = propValue(node, 'parameters', context) ?? []
    const returns = propValue(node, 'returns', context)
    const sections = [
      referenceHeading(node, context),
      description,
      fencedCode(signature, 'typescript'),
    ]

    if (parameters.length) {
      sections.push(
        `${'#'.repeat(Number(propValue(node, 'heading-level', context) ?? 2) + 1)} Parameters`,
        renderApiTable(parameters, 'Parameter'),
      )
    }

    if (returns) {
      sections.push(
        `${'#'.repeat(Number(propValue(node, 'heading-level', context) ?? 2) + 1)} Returns`,
        `\`${escapeTableCell(returns.type)}\` — ${returns.description}`,
      )
    }

    return sections.filter(Boolean).join('\n\n')
  }

  if (node.tag === 'TypeApiReference') {
    const sections = [
      referenceHeading(node, context),
      propValue(node, 'description', context),
      fencedCode(propValue(node, 'definition', context), 'typescript'),
    ]
    const properties = propValue(node, 'properties', context) ?? []

    if (properties.length) {
      sections.push(renderApiTable(properties))
    }

    const content = renderChildren(node.children ?? [], context)

    if (content) {
      sections.push(content)
    }

    return sections.filter(Boolean).join('\n\n')
  }

  if (node.tag === 'DocsComponentRecommendation') {
    const title = propValue(node, 'title', context) ?? 'Related component'
    const componentName = propValue(node, 'component-name', context)
    const componentHref = propValue(node, 'component-href', context)
    const recommendation = normalizeInline((node.children ?? []).map(inlineContent).join(''))
    const component = componentName && componentHref
      ? `[${componentName}](${absoluteDocumentationLink(componentHref)})`
      : componentName
    const heading = component ? `${title}: ${component}` : title

    return `> **${heading}**\n>\n> ${recommendation}`
  }

  if (node.tag === 'Head') {
    return ''
  }

  return renderChildren(node.children ?? [], context)
}

function renderChildren(children, context) {
  return children
    .map(child => renderNode(child, context))
    .filter(Boolean)
    .join('\n\n')
}

function addContentsIfLong(markdown) {
  if (markdown.split('\n').length <= 100) {
    return markdown
  }

  const headings = [...markdown.matchAll(/^## (.+)$/gm)].map(match => match[1])

  if (headings.length < 2) {
    return markdown
  }

  const contents = [
    '## Contents',
    '',
    ...headings.map(heading => `- [${heading}](#${markdownAnchor(heading)})`),
    '',
  ].join('\n')
  const firstSection = markdown.indexOf('\n## ')

  return `${markdown.slice(0, firstSection + 1)}${contents}\n${markdown.slice(firstSection + 1)}`
}

function parseDocumentationPage(pagePath, href) {
  const source = readFileSync(pagePath, 'utf8')
  const { descriptor } = parse(source, { filename: pagePath })

  if (!descriptor.template?.ast || !descriptor.scriptSetup) {
    throw new Error(`Expected template and script setup in ${pagePath}`)
  }

  const context = {
    ...createScriptContext(descriptor.scriptSetup.content, pagePath),
    pagePath,
  }
  const docsPage = findElement(descriptor.template.ast, 'DocsPage')

  if (!docsPage) {
    throw new Error(`DocsPage not found in ${pagePath}`)
  }

  const title = propValue(docsPage, 'title', context)
  const description = propValue(docsPage, 'description', context)

  if (typeof title !== 'string' || typeof description !== 'string') {
    throw new Error(`Static title and description are required in ${pagePath}`)
  }

  const body = renderChildren(docsPage.children ?? [], context)
  const sourceLink = `${hostedDocsBase}${href}`
  let markdown = [
    generatedNotice,
    '',
    `# ${title}`,
    '',
    description,
    '',
    body,
    '',
    `Hosted documentation: [${title}](${sourceLink})`,
    '',
  ].join('\n')

  markdown = addContentsIfLong(markdown)

  return { description, markdown, title }
}

function parseComponentPage(componentName) {
  const slug = componentSlug(componentName)
  const pagePath = join(componentPagesDirectory, `${componentName}.vue`)
  const page = parseDocumentationPage(pagePath, `/docs/components/${slug}`)

  return { ...page, componentName, slug }
}

function parseFrontendPage(definition) {
  const pagePath = join(frontendPagesDirectory, definition.source)
  const page = parseDocumentationPage(pagePath, definition.href)

  return {
    ...definition,
    ...page,
    output: definition.output ?? `${definition.outputPrefix}-${componentSlug(page.title)}.md`,
  }
}

function buildCatalog(pages, dataTable) {
  const rows = [...pages, dataTable]
    .sort((a, b) => a.title.localeCompare(b.title))
    .map(page => {
      const reference = page.componentName === 'DataTable'
        ? 'Backend/PHP guidance deferred'
        : `[Read](${page.slug}.md)`
      return `| ${page.title} | ${page.description} | ${reference} |`
    })

  return [
    generatedNotice,
    '',
    '# Component catalog',
    '',
    'Use this catalog to select an existing component before writing a custom one. Read only the detailed component reference relevant to the current task. Registry names use the `@stacktrace/<kebab-case-name>` namespace.',
    '',
    '| Component | Use it for | Detailed guidance |',
    '| --- | --- | --- |',
    ...rows,
    '',
    '## Choosing close alternatives',
    '',
    '- Use **Alert Dialog** for an interruptive decision and **Confirmation Dialog** for the application-wide programmatic confirmation flow.',
    '- Use **Dialog** for focused modal work, **Sheet** for complementary edge content, and **Drawer** for a touch-oriented sliding panel.',
    '- Use **Native Select** for native platform behavior, **Select** for a styled list, and **Combobox** when searching a larger set matters.',
    '- Use **Table** for semantic client-rendered tables and **Data Table** when PHP owns columns, filters, actions, sorting, or pagination.',
    '- Use **Checkbox** for independent choices, **Radio Group** for one choice among several, and **Switch** for an immediately applied setting.',
    '- Use **Button Group** only when related buttons form one compact control; keep independent actions such as Save and Cancel separate.',
    '',
    'Inspect the installed component source before using props or emits not covered by its reference. Data Table and its PHP API remain in the separate backend reference pass.',
    '',
  ].join('\n')
}

function buildFrontendCatalog(pages) {
  const rows = pages.map(page => (
    `| ${page.category} | ${page.title} | ${page.description} | [Read](${page.output}) |`
  ))

  return [
    generatedNotice,
    '',
    '# Frontend package API catalog',
    '',
    'Use this catalog when working with the `@stacktrace/ui` frontend package shipped by Composer. Load only the guide or API reference relevant to the task.',
    '',
    '| Category | Export | Use it for | Detailed guidance |',
    '| --- | --- | --- | --- |',
    ...rows,
    '',
    'Import runtime exports from `@stacktrace/ui` and use `import type` for type-only exports. Do not install a separate npm package for this API.',
    '',
  ].join('\n')
}

function syncFile(path, content) {
  const existing = existsSync(path) ? readFileSync(path, 'utf8') : undefined

  if (existing === content) {
    return
  }

  if (checkOnly) {
    failures.push(`Out of date: ${relative(rootDirectory, path)}`)
    return
  }

  mkdirSync(dirname(path), { recursive: true })
  writeFileSync(path, content)
}

const componentNames = readdirSync(componentsDirectory)
  .filter(name => statSync(join(componentsDirectory, name)).isDirectory())
  .sort()
const pages = componentNames
  .filter(name => !skippedComponents.has(name))
  .map(parseComponentPage)
const dataTable = parseComponentPage('DataTable')
const frontendPages = frontendPageDefinitions.map(parseFrontendPage)
const expectedFiles = new Set([
  ...pages.map(page => `${page.slug}.md`),
  ...frontendPages.map(page => page.output),
  'components.md',
  'frontend-api.md',
])

for (const page of pages) {
  syncFile(join(outputDirectory, `${page.slug}.md`), page.markdown)
}

syncFile(join(referencesDirectory, 'components.md'), buildCatalog(pages, dataTable))

for (const page of frontendPages) {
  syncFile(join(outputDirectory, page.output), page.markdown)
}

syncFile(join(referencesDirectory, 'frontend-api.md'), buildFrontendCatalog(frontendPages))

if (existsSync(outputDirectory)) {
  for (const file of readdirSync(outputDirectory).filter(file => file.endsWith('.md'))) {
    if (expectedFiles.has(file)) {
      continue
    }

    const path = join(outputDirectory, file)
    const content = readFileSync(path, 'utf8')

    if (!content.startsWith(generatedNotice)) {
      continue
    }

    if (checkOnly) {
      failures.push(`Stale generated file: ${relative(rootDirectory, path)}`)
    } else {
      unlinkSync(path)
    }
  }
}

if (existsSync(legacyOutputDirectory)) {
  if (checkOnly) {
    failures.push(`Stale generated directory: ${relative(rootDirectory, legacyOutputDirectory)}`)
  } else {
    rmSync(legacyOutputDirectory, { recursive: true, force: true })
  }
}

if (failures.length) {
  console.error(failures.join('\n'))
  process.exit(1)
}

console.log(`${checkOnly ? 'Verified' : 'Generated'} ${pages.length} component and ${frontendPages.length} frontend package skill references.`)
